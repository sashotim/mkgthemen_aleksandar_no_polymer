            <p>
Beim Ameloblastom handelt es sich um einen benignen epithelialen odontogenen Tumor. Die Ätiologie ist unklar. Man nimmt an, dass sie von Resten der Zahnleiste ausgehen (Malassez-Epithelresten). Die Inzidenz ist geschlechtsunspezifisch und kann in jedem Lebensalter auftreten. Am häufigsten treten sie jedoch im 3.-5. Lebensjahrzehnt auf. Der Unterkiefer ist 5x häufiger betroffen als der Oberkiefer. Das Ameloblastom mach 10-20% der odontogenen Tumoren aus.&nbsp;
</p>
