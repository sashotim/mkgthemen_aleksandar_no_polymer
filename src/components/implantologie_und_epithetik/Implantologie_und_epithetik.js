            <p>Bei geringem Anteil kortikalen Knochens und <strong>mangelnder Dichte</strong> der Spongiosa wird mittels
              Osteomen/Bone Spreader mit steigendem Durchmesser die Implantataufbereitung mittels rotierender Instrumente
              ersetzt.</p>
